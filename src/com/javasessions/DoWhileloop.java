package com.javasessions;

public class DoWhileloop {

	public static void main(String[] args) {
		
		int i = 1;
		
		do {
			System.out.println(i);
			i++;
			if(i==7) {
				
				System.out.println("Hello ");
			}
		}
		
		while(i<=10) ;
		System.out.println("-------------");
		
	for(int k = 1;k<=100;k++) {
		if(k%2==0) {
			System.out.println(k);
		}
		
	}

}}
